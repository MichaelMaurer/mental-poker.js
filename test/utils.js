import test from 'ava';
import * as Utils from './../src/utils';

test('intToByteArray', (t) => {
  t.deepEqual(Utils.intToByteArray(0x00000000), [0x00, 0x00, 0x00, 0x00]);
  t.deepEqual(Utils.intToByteArray(0x0000007f), [0x00, 0x00, 0x00, 0x7f]);

  t.deepEqual(Utils.intToByteArray(0x000000ff), [0x00, 0x00, 0x00, 0xff]);
  t.deepEqual(Utils.intToByteArray(0x00000100), [0x00, 0x00, 0x01, 0x00]);

  t.deepEqual(Utils.intToByteArray(0x0000ff00), [0x00, 0x00, 0xff, 0x00]);
  t.deepEqual(Utils.intToByteArray(0x00010000), [0x00, 0x01, 0x00, 0x00]);

  t.deepEqual(Utils.intToByteArray(0xffffffff), [0xff, 0xff, 0xff, 0xff]);

  t.deepEqual(Utils.intToByteArray(0xdeadbeef), [0xde, 0xad, 0xbe, 0xef]);
});
